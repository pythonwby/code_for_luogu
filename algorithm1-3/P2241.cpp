#include <cstdio>

//a:正方形个数,b:长方形个数,n:棋盘长度,m:棋盘宽度
int a,b,n,m;

int main(){
    scanf("%d%d",&n,&m);
    for(int c=0;c<n;c++){
        for(int d=0;d<m;d++){
            for(int i=0;i<n;i++){
                for(int j=0;j<m;j++){
                    if(i==j) a++;
                    else b++;
                }
            }
        }
    }
    
    printf("%d %d",a,b);
    system("pause");
    return 0;
}